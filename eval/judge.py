"""LLM-as-judge metrics — Tier B. Spends API tokens; only runs under --judge / `make eval-judge`.

A judge is one extra LLM call that scores an output against a rubric and returns a structured
`JudgeVerdict` (via the same `complete_structured` / `messages.parse` path core/ uses). The judge
model MUST differ from the judged model: the memo is generated by Sonnet, so the judge is Opus
(config.judge_model) — don't let a model grade its own blind spots (phase-6 IMPLEMENTATION §6).

Groundedness is the legal-integrity metric: the judge sees an obligation claim plus the statute
text it cites, and decides whether the claim is supported by THAT text — the check that catches a
plausible-sounding but hallucinated obligation, the worst failure for a compliance tool.
"""

from dataclasses import dataclass, field
from typing import Literal

from pydantic import BaseModel

from eval.harness import locate_section
from patchwork_assurance.core.contracts import ComplianceMemo, Msg

JUDGE_SYSTEM = (
    "You are a careful legal-evaluation judge. You are given an OBLIGATION CLAIM written by a "
    "compliance tool and the STATUTE TEXT it cites. Decide whether the claim is supported by THAT "
    "text only — not by your own legal knowledge. Return 'yes' if the claim is fully supported by "
    "the text, 'partial' if it is only partly supported or overstates what the text says, and 'no' "
    "if the text does not support the claim (a hallucinated or unsupported obligation). List the "
    "specific unsupported pieces in unsupported_claims. Be strict but fair: hedged, accurate "
    "summaries of the text are 'yes'."
)


class JudgeVerdict(BaseModel):
    grounded: Literal["yes", "partial", "no"]
    reason: str
    unsupported_claims: list[str] = []


def _grounding_user(claim: str, statute_text: str) -> str:
    return f"## OBLIGATION CLAIM\n{claim}\n\n## CITED STATUTE TEXT\n{statute_text}\n"


def judge_groundedness(claim: str, statute_text: str, llm) -> JudgeVerdict:
    """One judged call: is `claim` supported by `statute_text`? `llm` is any LLMClient (Opus in
    production; a stub returning a fixed verdict in tests)."""
    return llm.complete_structured(
        JUDGE_SYSTEM,
        [Msg(role="user", content=_grounding_user(claim, statute_text))],
        JudgeVerdict,
    )


@dataclass
class GroundednessOutcome:
    case_id: str
    judged: int  # obligations whose cited section text we could locate and judge
    grounded_yes: int
    verdicts: list[tuple[str, str]] = field(default_factory=list)  # (citation, grounded)
    unsupported: list[str] = field(default_factory=list)


def score_groundedness(
    memo: ComplianceMemo, section_texts: dict[str, dict[str, str]], llm, case_id: str = ""
) -> GroundednessOutcome:
    """Judge every memo obligation against the text of the section it cites. Obligations whose
    citation doesn't resolve to a real section are skipped here (citation-exists already flags
    those) — groundedness only judges claims we can pair with real statute text."""
    sections = {jurisdiction: set(texts) for jurisdiction, texts in section_texts.items()}
    out = GroundednessOutcome(case_id, judged=0, grounded_yes=0)
    for finding in memo.per_law:
        for obligation in finding.obligations:
            located = locate_section(obligation.citation, sections)
            if located is None:
                continue
            jurisdiction, section = located
            verdict = judge_groundedness(obligation.text, section_texts[jurisdiction][section], llm)
            out.judged += 1
            if verdict.grounded == "yes":
                out.grounded_yes += 1
            out.verdicts.append((obligation.citation, verdict.grounded))
            out.unsupported += verdict.unsupported_claims
    return out
